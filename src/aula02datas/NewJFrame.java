    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        System.out.println("");
        
        
        
        Date d;
        d = jDateChooser2.getDate();
        Calendar c = Calendar.getInstance();
        c.setTime(d);
        
        SimpleDateFormat sdf;
        sdf = new SimpleDateFormat("dd/MM/yyyy");
        
        DecimalFormat df;
        df = new DecimalFormat("R$ #,##0.00");
        int p;
        p = Integer.parseInt(txtParcelas.getText());
        
        //gerando valor das parcelas
        Float valor = Float.parseFloat(txtValor.getText());
        Float ps = valor/p;
        
        
        Object[][] l;
        Object[] col;
        col=new Object[2];
        l=new Object[p][2];
        col[0]="Valor";
        col[1]="Data";
        for (int i = 0; i < p; i++) {
            c.set(Calendar.DAY_OF_MONTH, c.get(Calendar.DAY_OF_MONTH) + (30));
            if (c.get(Calendar.DAY_OF_WEEK)==1)
                c.set(Calendar.DAY_OF_MONTH, c.get(Calendar.DAY_OF_MONTH) + (1));
            if (c.get(Calendar.DAY_OF_WEEK)==7)
                c.set(Calendar.DAY_OF_MONTH, c.get(Calendar.DAY_OF_MONTH) + (2));
            
            l[i][0]=df.format(ps);
            l[i][1]= sdf.format(c.getTime());
        }
        jTable1.setModel(new DefaultTableModel(l, col));
    }//GEN-LAST:event_jButton2ActionPerformed

